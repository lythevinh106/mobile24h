                Hiệu năng khủng với chip M1

                iPad Pro 12.9” 2021 được trang bị chipset Apple M1 giống với Macbook Pro mới. Con chip này hỗ trợ CPU 8 nhân và GPU 8 nhân, đem lại hiệu suất CPU tăng 50%, GPU nhanh hơn đến 40% so với chip A12Z của phiên bản năm ngoái.

                Theo nhà sản xuất, sự thay đổi này sẽ khiến iPad Pro 12.9” 2021 có sức mạnh ưu việt, thậm chí còn vượt trội hơn các dòng laptop thông thường.

                Ngoài ra, sản phẩm hỗ trợ RAM 8GB và dung lượng bộ nhớ 256GB để người dùng thoải mái lưu trữ dữ liệu, quay chụp video thỏa thích.
                <img src="images/tab2.jpg" style="display: block;" alt="">


                Hệ thống camera đa tính năng

                iPad Pro M1 12.9" (2021) sở hữu camera sau vẫn là cụm hình vuông gồm 2 ống kính và 1 cảm biến. Trong đó gồm 1 camera sử dụng cảm biến 12MP, 1 ống kính góc siêu rộng 10MP cùng với một máy quét LiDAR. Cảm biến LiDAR mang đến cho người dùng những trải nghiệm chụp ảnh và quay video hoàn hảo trong điều kiện thiếu sáng.

                Ở mặt trước iPad Pro 12.9 inch 2021 là camera siêu rộng với cảm biến 12MP và góc chụp lên đến 122 độ có khả năng thu nhỏ, phóng to và xoay khung hình để đảm bảo bạn có những góc chụp đúng trọng tâm, thu gọn và mở rộng để lấy đầy đủ chi tiết nhất. Ống kính này cũng hỗ trợ tính năng TrueDepth giúp mở khóa bằng nhận diện khuôn mặt siêu nhạy.

                <img src="images/tab3.jpg" style="display: block;" alt="">
                Cổng Thunderbolt chuyên nghiệp, hỗ trợ kết nối mạng Wifi 6 thế hệ mới

                iPad Pro M1 12.9" (2021) Wifi - 256GB được trang bị cổng kết nối đa năng Thunderbolt dưới dạng USB-C. Với tốc độ băng thông khi kết nối có dây lên đến 40Gb/s, người dùng có thể tải mọi tệp tin trong chớp mắt, xem video 4K trực tuyến mượt mà. Bên cạnh đó, cổng kết nối cũng cho phép kết nối với nhiều phụ kiện, kể cả xuất ra màn hình lớn.

                Tuy không hỗ trợ 5G nhưng bù lại, thiết bị này được nâng cấp hỗ trợ kết nối Wifi 6 thế hệ mới. Vì thế, chúng ta hoàn toàn yên tâm rằng việc kết nối và sử dụng cũng sẽ nhanh chóng và ổn định hơn nhiều so với thế hệ tiền nhiệm
